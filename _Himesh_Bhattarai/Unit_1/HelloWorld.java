// Unit 1: Java Basics - Hello World
// Concept: First Java program structure

public class HelloWorld {

    // main() is the entry point of Java program
    // String[] args receives command line arguments
    // static means it doesn't need an object to run
    public static void main(String[] args) {

        // print() outputs text without new line
        System.out.print("Hello ");

        // println() outputs text with new line
        System.out.println("World!");

        // Variables store data
        String name = "Himesh";
        System.out.println("Name: " + name);
    }
}
